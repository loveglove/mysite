<script>
	
        var first = true

		var wavesurfer = WaveSurfer.create({
		    container: '#waveform',
		    waveColor: 'silver',
		    progressColor: 'black',
		    barWidth: 2,
		});

		wavesurfer.on('ready', function () {
            if(!first){
                wavesurfer.play();
            }
            first = false;
		});

		wavesurfer.on('play', function () {
    		$(".list-group-item.active").find('.icon').removeClass('fa-play').addClass('fa-pause');
		});

		wavesurfer.on('pause', function () {
    		$(".list-group-item.active").find('.icon').removeClass('fa-pause').addClass('fa-play');
		});

		wavesurfer.on('finish', function () {
			var item = $(".list-group-item.active").next()
			var path = item.attr('href');
			$(".list-group-item").removeClass('active');
			item.addClass('active');
			$(".list-group-item").find('.icon').removeClass('fa-pause').addClass('fa-play')
			$(".progress-bar").css('width', '0%');
		    wavesurfer.load(path);

		});


		wavesurfer.on('loading', function (progress, event) {
    		console.log(progress);
    		if(progress > 99){
    			setTimeout(function(){
    				$(".progress-container").hide();
    			},3000);
    		}else{
    			$(".progress-container").show();
    			$(".progress-bar").css('width', progress+'%');
    		}
		});



		$(".list-group-item").on('click', function(){
			
			var item = $(this);
			var path = item.attr('href');

			if(item.hasClass('active')){
				wavesurfer.playPause();
			}else{
				$(".list-group-item").removeClass('active');
				item.addClass('active');
                $(".list-group-item").find('.icon').removeClass('fa-pause').addClass('fa-play');
				wavesurfer.load(path);
			}

			return false;
		});

        $(document).ready(function(){
            wavesurfer.load('/audio/as/Glover - Days End.mp3');
        });



</script>

